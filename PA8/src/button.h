#pragma once

class Button
{
public:

  void setup()
  {
    rcu_periph_clock_enable(RCU_GPIOA);
    gpio_init(GPIOA, GPIO_MODE_IN_FLOATING, GPIO_OSPEED_50MHZ, GPIO_PIN_8);
  }

  bool operator()()
  {
    return gpio_input_bit_get(GPIOA, GPIO_PIN_8) ;
  }
  
} ;

