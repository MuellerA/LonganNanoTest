extern "C"
{
#include "gd32vf103.h"
}
#include <stdio.h>

#include "led.h"
#include "button.h"

RgbLed led ;
Button button ;

int main()
{
  led.setup() ;
  button.setup() ;

  while (true)
  {
    if (button())
      led.green() ;
    else
      led.red() ;
  }
}

